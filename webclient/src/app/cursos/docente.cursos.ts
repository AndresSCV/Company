export class Docente{


	public nombre:string;
	public apellidos:string;
	public honorarios:number;
	public vinculado:boolean;

	constructor(nombre_, apellidos_, honorarios_, vinculado_){
		this.nombre = nombre_;
		this.apellidos = apellidos_;
		this.honorarios = honorarios_;
		this.vinculado = vinculado_;
	}


	

}